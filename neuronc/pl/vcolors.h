#define BLUE	0x01
#define GREEN	0x02
#define CYAN	0x03
#define RED	0x04
#define MAGENTA	0x05
#define BROWN	0x06
#define WHITE	0x07

#define IBLUE	0x09
#define IGREEN	0x12
#define ICYAN	0x1b
#define IRED	0x24
#define IMAGENTA 0x2d
#define YELLOW	0x36
#define IWHITE	0x3f
#define BWHITE	0x38

